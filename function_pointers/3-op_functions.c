#include "3-calc.h"

/**
 * op_add - adding numbers
 * @a: num1
 * @b: num2
 *
 * Return: sum of numbers
 */

int op_add(int a, int b)

{
	return (a + b);

}

/**
 * op_sub - subtracting numbers
 * @a: num1
 * @b: num2
 *
 * Return: diff of numbers
 */

int op_sub(int a, int b)

{
	return (a - b);
}

/**
 * op_mul - multiplying numbers
 * @a: num1
 * @b: num2
 *
 * Return: product of numbers
 */

int op_mul(int a, int b)

{
	return (a * b);

}

/**
 * op_div - dividing numbers
 * @a: num1
 * @b: num2
 *
 * Return: result of division of numbers
 */

int op_div(int a, int b)

{
	return (a / b);
}

/**
 * op_mod - modulo of numbers
 * @a: num1
 * @b: num2
 *
 * Return: result of modulo of numbers
 */

int op_mod(int a, int b)

{
	return (a % b);

}
